n1=int(input("enter first number:"))
n2=int(input("enter second number:"))

#arithmetic operator
print(n1+n2) #addition
print(n1-n2) #subtraction
print(n1/n2) #division
print(n1//n2) #floor division
print(n1*n2) #multiplication
print(n1%n2) #modulo

#comparison
print(n1>n2) #greter then
print(n1>=n2) #greter then equal to
print(n1<n2) #less then
print(n1<=n2) #less then equal to
print(n1==n2) #equal to
print(n1 != n2) #not equal to

#logical operator
print(n1 > n2 and n1==10 ) #and operator #return true if both condition true
print(n1 > n2 or n1==10 ) #or operator #return true if any one condition true
print( not n1==10) #not #if condition true return false and if condition false return true
